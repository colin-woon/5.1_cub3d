#include "libft.h"
/*
Checks for alphabets
Input: ASCII code digit
Output: 1 or 0
Compares c to ASCII table of lowercase and uppercase alphabets
 */
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
