#include "libft.h"
/*
Changes lowercase character to uppercase
Input: ASCII code `c`, presumably lowercase characters
Output: ASCII code `c`
Decrements `c` with 32 to get its uppercase counterpart if
lowercase letters are detected.
 */
int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		c -= 32;
	return (c);
}
