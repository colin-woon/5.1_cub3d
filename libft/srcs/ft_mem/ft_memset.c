#include "libft.h"
/*
Fill a block of memory with a particular value. Memory set.
Input: Generic pointer `s`, any integer `c`, memory size in bytes `n`
Output: Original pointer of `s`
The function iterates over each byte of the memory block pointed by `s` for `n`
bytes, while setting each byte to the value of an integer, `c` (each integer
is 1 byte). Then returns the original pointer of `s`.
 */
void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (i < n)
	{
		str[i] = c;
		i++;
	}
	return (s);
}
